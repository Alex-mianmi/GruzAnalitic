function setChaptersOpen(open) {
  document.body.classList.toggle("chapters-open", open);
  $("#chapters-button").setAttribute("aria-expanded", String(open));
}

function setSite(site) {
  if (!SITES[site]) return;
  state.site = site;
  state.slide = 0;
  setChaptersOpen(false);
  render();
}

function setSlide(index) {
  const max = SITES[state.site].slides.length - 1;
  state.slide = Math.max(0, Math.min(index, max));
  setChaptersOpen(false);
  renderNavigation();
  renderSlide();
}

function readHash() {
  const [site, rawSlide] = location.hash.replace("#", "").split("/");
  if (SITES[site]) state.site = site;
  const slide = Number(rawSlide) - 1;
  if (Number.isFinite(slide)) {
    state.slide = Math.max(0, Math.min(slide, SITES[state.site].slides.length - 1));
  }
}

