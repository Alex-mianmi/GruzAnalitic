function renderSlide() {
  const site = SITES[state.site];
  const slide = site.slides[state.slide];
  document.documentElement.style.setProperty("--accent", site.accent);
  document.documentElement.style.setProperty("--accent-rgb", site.rgb);
  document.body.classList.add("site-animated");
  $("#slide-frame").dataset.slideType = slide.type;
  $("#slide").innerHTML = renderers[slide.type](slide);
  $("#slide").scrollTop = 0;
  $("#prev-button").disabled = state.slide === 0;
  $("#next-button").disabled = state.slide === site.slides.length - 1;
  history.replaceState(null, "", `#${state.site}/${state.slide + 1}`);
  activateMotion();
}

function animateCount(element) {
  const original = element.textContent.trim();
  const match = original.match(/(\d(?:[\d ]*\d)?)(?:,(\d+))?/);
  if (!match) return;

  const integer = match[1].replaceAll(" ", "");
  const decimals = match[2]?.length ?? 0;
  const target = Number(`${integer}${match[2] ? `.${match[2]}` : ""}`);
  if (!Number.isFinite(target)) return;

  const before = original.slice(0, match.index);
  const after = original.slice(match.index + match[0].length);
  const duration = 850;
  const start = performance.now();

  const draw = (now) => {
    const progress = Math.min((now - start) / duration, 1);
    const eased = 1 - Math.pow(1 - progress, 4);
    const current = target * eased;
    const formatted = new Intl.NumberFormat("ru-RU", {
      minimumFractionDigits: decimals,
      maximumFractionDigits: decimals,
    }).format(current);
    element.textContent = `${before}${formatted}${after}`;
    if (progress < 1) requestAnimationFrame(draw);
  };

  requestAnimationFrame(draw);
}

function activateMotion() {
  if (matchMedia("(prefers-reduced-motion: reduce)").matches) return;
  $("#slide")
    .querySelectorAll(".count-value")
    .forEach((element) => animateCount(element));
}

const slideFrame = $("#slide-frame");
slideFrame.addEventListener("pointermove", (event) => {
  if (matchMedia("(prefers-reduced-motion: reduce)").matches) return;
  const rect = slideFrame.getBoundingClientRect();
  const x = ((event.clientX - rect.left) / rect.width) * 100;
  const y = ((event.clientY - rect.top) / rect.height) * 100;
  slideFrame.style.setProperty("--pointer-x", `${x}%`);
  slideFrame.style.setProperty("--pointer-y", `${y}%`);
  slideFrame.style.setProperty("--tilt-x", `${(50 - y) * 0.025}deg`);
  slideFrame.style.setProperty("--tilt-y", `${(x - 50) * 0.025}deg`);
});

slideFrame.addEventListener("pointerleave", () => {
  slideFrame.style.setProperty("--pointer-x", "72%");
  slideFrame.style.setProperty("--pointer-y", "22%");
  slideFrame.style.setProperty("--tilt-x", "0deg");
  slideFrame.style.setProperty("--tilt-y", "0deg");
});

